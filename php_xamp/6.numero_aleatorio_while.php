6️⃣ Mostrar números del 10 al 1 con `while`

<?php
// Mostrar números del 10 al 1 con un bucle while
echo "<br>";
$i = 10; // Inicializamos la variable en 10

while ($i >= 1) { // Mientras $i sea mayor o igual a 1
    echo $i . "<br>"; // Imprimimos el número seguido de un salto de línea
    $i--; // Decrementamos $i en cada iteración
}
?>
